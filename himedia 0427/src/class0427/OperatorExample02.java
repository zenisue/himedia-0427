package class0427;

public class OperatorExample02 {
	public static void main(String[] args) {
		int x = 5;
		int y = 8;
		int result1 = ++x + 10;
		int result2 = y++ + 10;
		int result3 = x + 10;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(result1);
		System.out.println(result2);
		System.out.println(result3);
		
	}
}
